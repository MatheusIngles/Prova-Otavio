/* Criei uma função para cada numero de 1 até 10 */
function FatorialDe1() {
   /* Aqui defini qual vai ser o numero com a fatorial */
   var numeroEscolhido = 1;
   /* Variavel para o resultado d fatorial */
   var Resultado = 1;

   /* Loop para qualcular o fatorial assim mutiplicando o resultado pelo i  e dando o resultado */
   for (let i = 1; i <= numeroEscolhido; i++) {
      Resultado *= i;
   }

   /* Aqui eu passo o resultado do fatorial */
   alert("O fatorial do número é: " + Resultado);
}

